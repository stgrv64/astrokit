#!/bin/bash

i2cset -y 1 0x20 0x00 0x00
i2cset -y 1 0x20 0x01 0x00
i2cset -y 1 0x21 0x00 0x00
i2cset -y 1 0x21 0x01 0x00

i=0

while true 
do
 i=$((i+1))
 if [ $i -eq 256 ] 
 then
  break
 fi
 echo $i

 i2cset -y 1 0x20 0x14 0x00
 i2cset -y 1 0x20 0x15 0x00

 i2cset -y 1 0x21 0x14 0x00
 i2cset -y 1 0x21 0x15 0x00

 i2cset -y 1 0x20 0x14 $i 
 i2cset -y 1 0x20 0x15 $i 
 i2cset -y 1 0x21 0x14 $i 
 i2cset -y 1 0x21 0x15 $i 

 sleep 1
done
